-- ============================================================================
-- Fantasy Ekadashi — database schema
-- ============================================================================
-- Run this file in the Supabase SQL editor (or `supabase db push`) once, on
-- a fresh project. It creates all tables, RLS policies, the storage bucket
-- used for screenshots, and the helper RPCs the app calls from server actions.
--
-- Conventions:
--   * Times are stored as `timestamptz`.
--   * `ekadashi_date` is a `date` (no time component) representing the
--     calendar day in the league's timezone.
--   * IDs are uuid generated by `gen_random_uuid()`.
-- ============================================================================

create extension if not exists pgcrypto;

-- ----------------------------------------------------------------------------
-- profiles: 1:1 with auth.users, adds display name + timezone preference.
-- ----------------------------------------------------------------------------
create table if not exists public.profiles (
    id           uuid primary key references auth.users(id) on delete cascade,
    display_name text not null,
    timezone     text not null default 'Asia/Kolkata',
    created_at   timestamptz not null default now()
);

-- Auto-create a profile row when a new auth user signs up.
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    insert into public.profiles (id, display_name, timezone)
    values (
        new.id,
        coalesce(
            new.raw_user_meta_data ->> 'display_name',
            split_part(new.email, '@', 1)
        ),
        coalesce(new.raw_user_meta_data ->> 'timezone', 'Asia/Kolkata')
    )
    on conflict (id) do nothing;
    return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
    after insert on auth.users
    for each row execute function public.handle_new_user();

-- ----------------------------------------------------------------------------
-- leagues
-- ----------------------------------------------------------------------------
create type league_status as enum ('open', 'active', 'completed');

create table if not exists public.leagues (
    id            uuid primary key default gen_random_uuid(),
    name          text not null,
    invite_code   text not null unique,
    created_by    uuid not null references public.profiles(id) on delete cascade,
    timezone      text not null default 'Asia/Kolkata',
    status        league_status not null default 'open',
    current_round int not null default 0,
    created_at    timestamptz not null default now()
);

-- ----------------------------------------------------------------------------
-- league_members
-- ----------------------------------------------------------------------------
create table if not exists public.league_members (
    league_id     uuid not null references public.leagues(id) on delete cascade,
    user_id       uuid not null references public.profiles(id) on delete cascade,
    seed          int,
    eliminated_at timestamptz,
    joined_at     timestamptz not null default now(),
    primary key (league_id, user_id)
);

create index if not exists idx_members_user on public.league_members(user_id);

-- ----------------------------------------------------------------------------
-- matches
-- ----------------------------------------------------------------------------
create type match_status as enum (
    'scheduled',          -- created; awaiting Ekadashi
    'awaiting_submissions', -- Ekadashi day, players need to submit
    'pending_review',     -- both submitted, one disputed
    'completed',          -- winner decided
    'walkover'            -- one player missed the deadline
);

create table if not exists public.matches (
    id             uuid primary key default gen_random_uuid(),
    league_id      uuid not null references public.leagues(id) on delete cascade,
    round          int not null,
    ekadashi_date  date not null,
    player_a       uuid references public.profiles(id) on delete set null,
    player_b       uuid references public.profiles(id) on delete set null,  -- null = bye
    winner_id      uuid references public.profiles(id) on delete set null,
    status         match_status not null default 'scheduled',
    created_at     timestamptz not null default now()
);

create index if not exists idx_matches_league_round on public.matches(league_id, round);
create index if not exists idx_matches_players on public.matches(player_a, player_b);

-- ----------------------------------------------------------------------------
-- submissions
-- ----------------------------------------------------------------------------
create type submission_source as enum ('ocr', 'manual', 'mixed');

create table if not exists public.submissions (
    id                    uuid primary key default gen_random_uuid(),
    match_id              uuid not null references public.matches(id) on delete cascade,
    player_id             uuid not null references public.profiles(id) on delete cascade,
    screen_time_minutes   int not null check (screen_time_minutes >= 0 and screen_time_minutes <= 24 * 60),
    screenshot_path       text,   -- path inside the 'screenshots' storage bucket
    source                submission_source not null default 'manual',
    disputed              boolean not null default false,
    dispute_note          text,
    submitted_at          timestamptz not null default now(),
    unique (match_id, player_id)
);

create index if not exists idx_subs_match on public.submissions(match_id);

-- ============================================================================
-- Row-level security
