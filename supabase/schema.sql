alter table public.profiles        enable row level security;
alter table public.leagues         enable row level security;
alter table public.league_members  enable row level security;
alter table public.matches         enable row level security;
alter table public.submissions     enable row level security;

-- Helper: is the current user a member of the given league?
create or replace function public.is_league_member(target_league uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select exists (
        select 1 from public.league_members
        where league_id = target_league and user_id = auth.uid()
    );
$$;

-- profiles --------------------------------------------------------------------
drop policy if exists "read own profile or shared league member" on public.profiles;
create policy "read own profile or shared league member"
    on public.profiles for select
    using (
        id = auth.uid()
        or exists (
            select 1
            from public.league_members m1
            join public.league_members m2 on m1.league_id = m2.league_id
            where m1.user_id = auth.uid() and m2.user_id = profiles.id
        )
    );

drop policy if exists "update own profile" on public.profiles;
create policy "update own profile"
    on public.profiles for update
    using (id = auth.uid())
    with check (id = auth.uid());

-- leagues ---------------------------------------------------------------------
drop policy if exists "members can read league" on public.leagues;
create policy "members can read league"
    on public.leagues for select
    using (
        created_by = auth.uid()
        or public.is_league_member(id)
    );

drop policy if exists "any user can create league" on public.leagues;
create policy "any user can create league"
    on public.leagues for insert
    with check (created_by = auth.uid());

drop policy if exists "creator can update league" on public.leagues;
create policy "creator can update league"
    on public.leagues for update
    using (created_by = auth.uid())
    with check (created_by = auth.uid());

-- league_members --------------------------------------------------------------
drop policy if exists "read members of leagues you belong to" on public.league_members;
create policy "read members of leagues you belong to"
    on public.league_members for select
    using (
        user_id = auth.uid()
        or public.is_league_member(league_id)
    );

drop policy if exists "self-insert when league open" on public.league_members;
create policy "self-insert when league open"
    on public.league_members for insert
    with check (
        user_id = auth.uid()
        and exists (
            select 1 from public.leagues
            where id = league_id and status = 'open'
        )
    );

drop policy if exists "leave league before it starts" on public.league_members;
create policy "leave league before it starts"
    on public.league_members for delete
    using (
        user_id = auth.uid()
        and exists (
            select 1 from public.leagues
            where id = league_id and status = 'open'
        )
    );

-- matches ---------------------------------------------------------------------
drop policy if exists "read matches in your leagues" on public.matches;
create policy "read matches in your leagues"
    on public.matches for select
    using (public.is_league_member(league_id));

-- (inserts/updates handled by service-role via server actions)

-- submissions -----------------------------------------------------------------
drop policy if exists "read submissions for your matches" on public.submissions;
create policy "read submissions for your matches"
    on public.submissions for select
    using (
        exists (
            select 1 from public.matches
            where id = submissions.match_id
            and public.is_league_member(matches.league_id)
        )
    );

drop policy if exists "insert your own submission" on public.submissions;
create policy "insert your own submission"
    on public.submissions for insert
    with check (
        player_id = auth.uid()
        and exists (
            select 1 from public.matches
            where id = match_id
            and (player_a = auth.uid() or player_b = auth.uid())
            and status in ('scheduled', 'awaiting_submissions')
        )
    );

drop policy if exists "update your own submission" on public.submissions;
create policy "update your own submission"
    on public.submissions for update
    using (player_id = auth.uid())
    with check (player_id = auth.uid());

-- ============================================================================
-- Storage bucket for screenshots
-- ============================================================================

-- Create a public bucket so screenshots can be displayed directly to opponents
-- via a public URL. (Files are still uploaded under a per-user folder; only
-- the URL is shared with the opponent.)
insert into storage.buckets (id, name, public)
values ('screenshots', 'screenshots', true)
on conflict (id) do nothing;

-- Storage RLS: users can upload only into their own folder.
drop policy if exists "users upload to own folder" on storage.objects;
create policy "users upload to own folder"
    on storage.objects for insert
    with check (
        bucket_id = 'screenshots'
        and (storage.foldername(name))[1] = auth.uid()::text
    );

drop policy if exists "public can read screenshots" on storage.objects;
create policy "public can read screenshots"
    on storage.objects for select
    using (bucket_id = 'screenshots');

drop policy if exists "users delete own screenshots" on storage.objects;
create policy "users delete own screenshots"
    on storage.objects for delete
    using (
        bucket_id = 'screenshots'
        and (storage.foldername(name))[1] = auth.uid()::text
    );
